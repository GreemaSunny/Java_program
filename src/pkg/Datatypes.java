package pkg;

public class Datatypes {

	public static void main(String[] args) {
		// Primitive datatypes
		
		byte a=127;
		System.out.println("Value of a= "+a);
		short s=2345;
		System.out.println(s);
		int i=3456788;
		System.out.println("Value of int= "+i);
		long lo=234567899l;
		System.out.println("Value of Long= "+lo);
		
		float fl=45.986f;
		System.out.println("Value of float= "+fl);
		double d=345689.345;
		System.out.println("Value of double= "+d);
		
		boolean bo=20<24;
		System.out.println("Value of boolean= "+bo);
		
		char c='A';
		System.out.println("Value of char= "+c);
		char c1=67;
		System.out.println("Value of char= "+c1);
	}

}
